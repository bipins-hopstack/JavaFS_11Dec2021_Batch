package assignment_1;

public class IncrementandDecrement {
	public static void main(String[] args) {
		int a=5;
		//Increment operator
		System.out.println("post increment of a still a "+ a++);
		
		System.out.println("post increment of a still a "+ ++a);
		
		//Decrement operator
		System.out.println("pre decrement "+ --a);
		System.out.println("pre decrement "+ a--);
	} 

}
